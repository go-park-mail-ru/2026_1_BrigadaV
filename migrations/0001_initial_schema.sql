CREATE TABLE IF NOT EXISTS "user" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    login TEXT NOT NULL UNIQUE,
    nickname TEXT NOT NULL UNIQUE,
    avatar_url TEXT,
    password_hash TEXT NOT NULL,
    country TEXT,
    city TEXT,
    about TEXT,
    has_reviews BOOLEAN DEFAULT false,
    role TEXT NOT NULL DEFAULT 'user',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS category (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    description TEXT,
    applicable_types TEXT[] NOT NULL DEFAULT '{attraction}',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS country (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS locality (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    country_id BIGINT NOT NULL REFERENCES country(id) ON DELETE CASCADE,
    latitude NUMERIC(10,8),
    longitude NUMERIC(11,8),
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE(name, country_id),
    CHECK (latitude >= -90 AND latitude <= 90),
    CHECK (longitude >= -180 AND longitude <= 180)
);

CREATE TABLE IF NOT EXISTS place (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    description TEXT,
    photo_url TEXT,
    type TEXT NOT NULL DEFAULT 'attraction',
    locality_id BIGINT REFERENCES locality(id) ON DELETE SET NULL,
    category_id BIGINT REFERENCES category(id) ON DELETE SET NULL,
    latitude DOUBLE PRECISION,
    longitude DOUBLE PRECISION,
    price BIGINT NOT NULL DEFAULT 0 CHECK (price >= 0),
    rating DECIMAL(2,1) NOT NULL DEFAULT 0,
    review_count INT NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS session (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES "user"(id) ON DELETE CASCADE,
    session_token_hash TEXT NOT NULL UNIQUE,
    expires_at TIMESTAMPTZ NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS favorite (
    user_id BIGINT NOT NULL REFERENCES "user"(id) ON DELETE CASCADE,
    place_id BIGINT NOT NULL REFERENCES place(id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    PRIMARY KEY (user_id, place_id)
);

CREATE TABLE IF NOT EXISTS photo (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    file_path TEXT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS place_photo (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    place_id BIGINT NOT NULL REFERENCES place(id) ON DELETE CASCADE,
    photo_id BIGINT NOT NULL REFERENCES photo(id) ON DELETE CASCADE,
    is_main BOOLEAN NOT NULL DEFAULT false,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_place_photo_main ON place_photo (place_id) WHERE is_main = true;

CREATE TABLE IF NOT EXISTS review (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES "user"(id) ON DELETE CASCADE,
    place_id BIGINT NOT NULL REFERENCES place(id) ON DELETE CASCADE,
    title TEXT,
    rating SMALLINT NOT NULL CHECK (rating >= 1 AND rating <= 5),
    comment TEXT,
    visit_date DATE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE(user_id, place_id)
);

CREATE TABLE IF NOT EXISTS trip (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title TEXT NOT NULL CHECK (char_length(title) >= 1 AND char_length(title) <= 255),
    description TEXT,
    location TEXT,
    start_date DATE,
    end_date DATE,
    preview_url TEXT,
    created_by BIGINT NOT NULL REFERENCES "user"(id) ON DELETE CASCADE,
    is_public BOOLEAN NOT NULL DEFAULT false,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CHECK (start_date <= end_date)
);

CREATE TABLE IF NOT EXISTS trip_member (
    trip_id BIGINT NOT NULL REFERENCES trip(id) ON DELETE CASCADE,
    user_id BIGINT NOT NULL REFERENCES "user"(id) ON DELETE CASCADE,
    role TEXT NOT NULL CHECK (role IN ('owner', 'editor', 'viewer')),
    joined_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    PRIMARY KEY (trip_id, user_id)
);

CREATE TABLE IF NOT EXISTS album (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    trip_id BIGINT NOT NULL REFERENCES trip(id) ON DELETE CASCADE,
    name TEXT NOT NULL CHECK (char_length(name) >= 1 AND char_length(name) <= 255),
    description TEXT,
    cover_photo_id BIGINT REFERENCES photo(id) ON DELETE SET NULL,
    max_photos INT DEFAULT 50,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS album_photo (
    album_id BIGINT NOT NULL REFERENCES album(id) ON DELETE CASCADE,
    photo_id BIGINT NOT NULL REFERENCES photo(id) ON DELETE CASCADE,
    order_index SMALLINT NOT NULL DEFAULT 0 CHECK (order_index >= 0),
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    PRIMARY KEY (album_id, photo_id)
);

CREATE TABLE IF NOT EXISTS trip_attractions (
    trip_id BIGINT NOT NULL REFERENCES trip(id) ON DELETE CASCADE,
    place_id BIGINT NOT NULL REFERENCES place(id) ON DELETE CASCADE,
    order_index SMALLINT NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    PRIMARY KEY (trip_id, place_id)
);


CREATE OR REPLACE FUNCTION update_place_rating() RETURNS TRIGGER AS $$
BEGIN
    IF TG_OP = 'DELETE' THEN
        UPDATE place SET
            rating = COALESCE((SELECT AVG(rating)::DECIMAL(2,1) FROM review WHERE place_id = OLD.place_id), 0),
            review_count = (SELECT COUNT(*) FROM review WHERE place_id = OLD.place_id),
            updated_at = NOW()
        WHERE id = OLD.place_id;
    ELSE
        UPDATE place SET
            rating = COALESCE((SELECT AVG(rating)::DECIMAL(2,1) FROM review WHERE place_id = NEW.place_id), 0),
            review_count = (SELECT COUNT(*) FROM review WHERE place_id = NEW.place_id),
            updated_at = NOW()
        WHERE id = NEW.place_id;
    END IF;
    RETURN NULL;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS review_rating_update ON review;
CREATE TRIGGER review_rating_update
    AFTER INSERT OR UPDATE OR DELETE ON review
    FOR EACH ROW EXECUTE FUNCTION update_place_rating();

CREATE OR REPLACE FUNCTION create_default_album() RETURNS TRIGGER AS $$
BEGIN
    INSERT INTO album (trip_id, name, description) VALUES (NEW.id, 'Основной альбом', '');
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trip_after_insert ON trip;
CREATE TRIGGER trip_after_insert AFTER INSERT ON trip FOR EACH ROW EXECUTE FUNCTION create_default_album();

